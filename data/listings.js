// In-memory state — mutated at runtime, resets on server restart
let listings = SEED_LISTINGS.map((l) => ({ ...l }))
let nextId = SEED_LISTINGS.length + 1

// Provider points tracked in-memory by provider name
const providerPoints = {}

export function getAvailableListings() {
  return listings.filter((l) => l.status === 'available')
}

export function getAllListings() {
  return listings
}

export function addListing(parsed) {
  const id = String(nextId++)
  const newListing = {
    id,
    title: parsed.title || 'Food',
    emoji: parsed.emoji || '🍽️',
    foodItems: parsed.foodItems || [],
    portions: Number(parsed.portions) || 1,
    portionsLeft: Number(parsed.portions) || 1,
    dietary: parsed.dietaryFlags || parsed.dietary || [],
    allergens: parsed.allergensPresent || parsed.allergens || [],
    pickupLocation: parsed.pickupLocation || '',
    area: parsed.pickupLocation || '',
    pickupWindow: parsed.pickupWindow || '',
    prepDate: parsed.prepDate || 'Today',
    status: 'available',
    providerName: parsed.providerName || 'Anonymous',
    cuisine: parsed.cuisine || 'Homemade',
    description: parsed.description || '',
    microwavable: parsed.microwavable !== false,
    distance: '? min walk',
    pinX: Math.floor(Math.random() * 70) + 15,
    pinY: Math.floor(Math.random() * 70) + 15,
    pointsValue: (Number(parsed.portions) || 1) * 2 + 5,
  }
  listings.push(newListing)
  return newListing
}

export function reserveListing(id) {
  const listing = listings.find((l) => l.id === id)
  if (!listing) return null
  listing.status = 'reserved'
  return listing
}

export function getProviderPoints(name) {
  return providerPoints[name] || 0
}

export function addProviderPoints(name, points) {
  providerPoints[name] = (providerPoints[name] || 0) + points
  return providerPoints[name]
}
